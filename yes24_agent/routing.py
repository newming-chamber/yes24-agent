_LONG_QUERY_CHARS = 45
_SENTENCE_END = re.compile(r"[.!?。？！]")
_MULTI_SENTENCE_MIN = 2


def is_identity_meta(message: str) -> bool:
    """질의가 정체성·메타 부류(_IDENTITY_META)인지 결정론으로 판정한다(부수효과 없음).

    무도구 즉답으로 끝나는 부류라, runner가 delta 홀드("출처 먼저 → 본문")를 스킵할지
    가르는 데 쓴다. 무도구=source 없음이라 순서 제약이 원천 성립불가하므로 홀드를 걷어
    첫 토큰부터 라이브로 흘려 죽은 대기를 없앤다. 판별은 classify_complexity와 같은
    `_IDENTITY_META` **기존 집합**만 재사용한다(신규 키워드·별도 리스트를 두지 않는다).
    """
    if not message:
        return False
    text = message.strip()
    lowered = text.lower()
    return any(kw in text or kw in lowered for kw in _IDENTITY_META)


def classify_complexity(message: str) -> str:
    """질의 1건을 FLASH/PRO로 분류한다(결정론, 부수효과 없음).

    pro 신호 키워드가 하나라도 있거나, 질의가 충분히 길거나(≥_LONG_QUERY_CHARS) 다절
    (문장부호 ≥_MULTI_SENTENCE_MIN)이면 PRO를 반환하고, 그 외 단문 단일질의는 FLASH를
    반환한다. 호출부(runner)는 반환값만으로 에이전트를 고른다.
    """
    if not message:
        return FLASH
    text = message.strip()
    lowered = text.lower()
    # 한글 키워드는 대소문자 무관, 라틴(' vs ' 등)은 lowered에서만 매칭된다.
    if any(kw in text or kw in lowered for kw in _PRO_KEYWORDS):
        return PRO
    if len(text) >= _LONG_QUERY_CHARS:
        return PRO
    if len(_SENTENCE_END.findall(text)) >= _MULTI_SENTENCE_MIN:
        return PRO
    return FLASH
